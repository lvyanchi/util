package cn.yclv.model.qq;

public class Group {
	private Long gc;
	private String gn;
	private Long owner;

	public Long getGc() {
		return gc;
	}

	public void setGc(Long gc) {
		this.gc = gc;
	}

	public String getGn() {
		return gn;
	}

	public void setGn(String gn) {
		this.gn = gn;
	}

	public Long getOwner() {
		return owner;
	}

	public void setOwner(Long owner) {
		this.owner = owner;
	}

}
